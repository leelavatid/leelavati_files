package basicsofjava;

public class RelationalOperatorsDemo {

	public static void main(String[] args) {
		float f1=0.12f,f2=0.15f;
		System.out.println("f1==f2 "+(f1==f2));//F
		System.out.println("f1<=f2 "+(f1<=f2));//T
		System.out.println("f1>=f2 "+(f1>=f2));//F
		System.out.println("f1<f2 "+(f1<f2));//T
		System.out.println("f1>f2 "+(f1>f2));//F
		System.out.println("f1!=f2 "+(f1!=f2));//T
		

	}

}
