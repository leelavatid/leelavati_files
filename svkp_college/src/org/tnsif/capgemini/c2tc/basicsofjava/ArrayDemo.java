package org.tnsif.capgemini.c2tc.basicsofjava;

public class ArrayDemo {

	public static void main(String[] args) {
		int num[]= {12,34,56,98};
		System.out.println(num[0]);
		System.out.println(num[3]);
		System.out.println(num[1]);
		byte b[]= {1,2,3,4,5,6};
		System.out.println(b[0]);
		System.out.println(b[4]);
		System.out.println(b[5]);
		short s[]= {10,20,30};
		System.out.println(s[0]);
		System.out.println(s[2]);
		System.out.println(s[1]);
		float f1[]= {0.9f,0.3f,7.90f};
		System.out.println(f1[0]);
		System.out.println(f1[1]);
		double d1[]= {12.34d,56.78d,90.45d,89.72d};
		System.out.println(d1[0]);
		System.out.println(d1[3]);
		System.out.println(d1[1]);
		char c[]= {'m','f','l','h'};
		System.out.println(c[0]);
		System.out.println(c[3]);
		System.out.println(c[1]);
		long l1[]= {5678987789765l,7876567849849838l,84434886663748l};
		System.out.println(l1[0]);
		System.out.println(l1[2]);
		System.out.println(l1[1]);
		
			
		}
	}


