package org.tnsif.capgemini.c2tc.basicsofjava;
import java.lang.*;
public class Built_In_PackagesDemo {

	public static void main(String[] args) {
		//upercase
		String progName="java programming language";
		System.out.println("The original prog name is:"+progName);
		System.out.println("The Upper case name is:"+progName.toUpperCase());
		//min number
         int min=Math.min(30,89);
         System.out.println("The Min Number is:"+min);
         //lowercase
         String personName="LEELAVATHI";
         System.out.println("The Original personName is:"+personName);
         System.out.println("the Lower case name is:"+personName.toLowerCase());
         //max number
         int max=Math.max(90, 2);
         System.out.println("The max num is:"+max);

	}

}
