package org.tnsif.capgemini.c2tc.basicsofjava;

public class AssignmentOperatorsDemo {

	public static void main(String[] args) {
		System.out.println("Shows the modify current value of variable");
	    int a=10;
	    //add and assign
	    a+=10;
	     System.out.println("a value after += "+a);//20
		//subtract and assign
	     a-=5;
	     System.out.println("a value after -= "+a);//15
	     //multiply and assign
	     a*=10;
	     System.out.println("a value after *= "+a);//150
	     //division AND assign
	     a/=50;
	     System.out.println("a value after /= "+a);//3
         //modules and assign
	     a%=2;
	     System.out.println("a value after %= "+a);//1
	     
	     
	     
	
	}

}
